	cout<<int(r)<<"\n";
	cout<<"but if we convert it to unsigned long long...\n";
	cout<<(unsigned long long)(r)<<"\n";
	cout<<"\n";
	cout<<"o=78234133\n";
	cout<<"o is not too big to convert to int\n";
	cout<<int(o)<<"\n";
	system("pause");
	system("cls");
